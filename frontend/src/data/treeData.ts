export const getNodesByLayer = (layer: TreeNode['layer']) =>
  Object.values(treeNodes).filter((n) => n.layer === layer);

export const getChildren = (nodeId: string) =>
  treeNodes[nodeId]?.children.map((id) => treeNodes[id]) || [];

export const getParent = (nodeId: string) => {
  const node = treeNodes[nodeId];
  return node?.parentId ? treeNodes[node.parentId] : null;
};

export const getSeason = (streak: number): 'winter' | 'spring' | 'summer' | 'autumn' => {
  if (streak >= 30) return 'autumn';
  if (streak >= 14) return 'summer';
  if (streak >= 7) return 'spring';
  return 'winter';
};

